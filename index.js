function somar(numero1, numero2){
    const resultadoDaSoma = numero1 + numero2
    console.log("A soma dos dois números é: " + resultadoDaSoma)
}

somar(5, 5)

function dividir(numero1, numero2){
    const resultadoDaDivisao = numero1 / numero2
    console.log("A divisão dos dois números é: " + resultadoDaDivisao)
}

dividir(10, 2)

function multiplicar(numero1, numero2){
    const resultadoDaMultiplicacao = numero1 * numero2
    console.log("A multiplicação dos dois números é: " + resultadoDaMultiplicacao)
}

multiplicar(10, 2)

function subtrair(numero1, numero2){
    const resultadoDaSubtracao = numero1 - numero2
    console.log("A subtração dos dois números é: " + resultadoDaSubtracao)
}

subtrair(10, 2)